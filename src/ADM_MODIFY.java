    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try
        {
            String bid=jTextField1.getText();
            String qry="select bookauth,bookpub,bookprc,bookcopies,booktit,bookstackno from tbbook where bookid='"+bid+"'";
            ResultSet rs =new DB().execute(qry);
            if(rs.next())
            {
                jTextField2.setText(rs.getString("booktit"));
                jTextField3.setText(rs.getString("bookauth"));
                jTextField4.setText(rs.getString("bookpub"));
                jTextField5.setText(rs.getString("bookprc"));
                jTextField6.setText(rs.getString("bookcopies"));
                jTextField7.setText(rs.getString("bookstackno"));
                jPanel2.setVisible(true);
                this.pack();
            }
            else
            {
                JOptionPane.showMessageDialog(null, "invalid book id");
            }
        }
        catch(Exception e)
        {
            System.out.println(e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

