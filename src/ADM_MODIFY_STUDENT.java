    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try
        {
            String qry="select stuname,stuphno,stustream,stusem from tbstudent where stuid='"+jTextField1.getText()+"'";
            ResultSet rs=new DB().execute(qry);
            if(rs.next())
            {
                jTextField2.setText(rs.getString("stuname"));
                jTextField3.setText(rs.getString("stuphno"));
                jTextField4.setText(rs.getString("stustream"));
                jTextField5.setText(rs.getString("stusem"));
                jPanel2.setVisible(true);
                this.pack();
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Invalid ID");
            }
        }
        catch(Exception e)
        {
            System.out.println(e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

